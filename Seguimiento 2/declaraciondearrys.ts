// Declaración de arrays
let scores: number[] = [10, 20, 30, 40, 50];
let cities: string[] = ['New York', 'Los Angeles', 'Chicago'];
let statuses: boolean[] = [false, true, false];
// Acceso a elementos del array
console.log(scores[0]); // Output: 10
console.log(cities[1]); // Output: 'Los Angeles'
console.log(statuses[2]); // Output: false
// Operaciones con arrays
scores.push(60); // Agrega un elemento al final del array
cities.pop(); // Elimina el último elemento del array
const total: number = scores.length; // Obtiene la longitud del array
console.log(total); // Output: 6